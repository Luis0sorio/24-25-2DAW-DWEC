/**
 * Opciones de plantear el ejercicio.
 * 1. La funcion recibe un valor con el nombre de la marca y devuelve objeto con los que encajen
 * 2. La funcion devuelve un objeto con los coches con el siguiente formato:
 * 'Honda' : [TODOS LOS COCHES CON HONDA],
 * 'Chevrolet' : [TODOS LOS COCHES CON CHEVROLET]
 * ...
 * 
 * Al no especificar el ejercicio, se puede interpretar de una u otra forma
 * siempre y cuando devuelva coches por marca.
 * 
 * En este caso, se realiza la opción 1 por ser más sencilla
 */

function ej9(marca){
    let objectCoches = new Object(); // Creamos el objeto coches que vamos a devolver
    let contador = 1; // Creamos un contador que usaremos como clave al insertar 
    // Recorremos los coches
    for (let i in coches) {
        // En caso de que la marca sea igual que la del coche, debemos insertarlo
        if(coches[i]['marca'] == marca){
            /**
             * Aqui al coincidir, debemos insertar el coche dentro de 'objectCoches'.
             * Seria interesante mirar que funciones de object pueden insertar valores
             */

            /**
             * Recordamos que el formato de objeto es el siguiente
             * {
             *   clave: valor
             * }
             * 
             * Por tanto buscar una funcion que genere esa insercion o hacerla manualmente
             */

            // https://developer.mozilla.org/en-US/docs/Web/JavaScript/Reference/Global_Objects/Object/defineProperty
            Object.defineProperty(objectCoches, contador, {
                value: coches[i],
                writable: false,
            });
            contador++;
        }
    }
    return objectCoches;
}

